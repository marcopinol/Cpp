#include <iostream>
using namespace std;

int main(){
	int i, j, k;
	
	cout << "Endereco de i = " << &i << endl; //& operator shows the memory address of his operand variable, in this case, i;
	cout << "Endereco de j = " << &j << endl;
	cout << "Endereco de k = " << &k << endl;
	
	system("PAUSE");
	return 0;
}
